
import math

def vlist(v):
    return ','.join(['float('+str(it)+')' for it in v])

def vparse(s):
    return [float(it) for it in s.split(' ')]

def vlength(v):
    return math.sqrt(sum([it*it for it in v]))

def vsum(v1, v2):
    return list(map(lambda a, b: a + b, v1, v2))

def vsub(v1, v2):
    return list(map(lambda a, b: a - b, v1, v2))

def vdiv(v, d):
    return [it / d for it in v]

def vmean(a):
    s = None
    if len(a) == 0:
        return s
    for it in a:
        if s is None:
            s = it
        else:
            s = vsum(s, it)
    return vdiv(s, len(a))

