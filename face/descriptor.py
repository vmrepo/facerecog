def vlist(v):
    return ','.join(['float('+str(it)+')' for it in v])

def vparse(s):
    return [float(it) for it in s.split(' ')]

def vlength(v):
    return math.sqrt(sum([it*it for it in v]))

def vsum(v1, v2):
    return list(map(lambda a, b: a + b, v1, v2))

def vsub(v1, v2):
    return list(map(lambda a, b: a - b, v1, v2))

def vmul(v, k):
    return [it * k for it in v]

def vdiv(v, d):
    return [it / d for it in v]

def vscalar(v1, v2):
    return sum(list(map(lambda a, b: a * b, v1, v2)))

def vmean(a):
    s = None
    if len(a) == 0:
        return s
    for it in a:
        if s is None:
            s = it
        else:
            s = vsum(s, it)
    return vdiv(s, len(a))

def vstd1(a):
    m = vmean(a)
    s = 0
    for it in a:
        s += vlength(vsub(it, m)) ** 2
    s /= len(a)
    return math.sqrt(s)

def vstd2(a):
    m = vmean(a)
    s = None
    for it in a:
        v = vsub(it, m)
        v = [it * it for it in v]
        if s is None:
            s = v
        else:
            s = vsum(s, v)
    s = vdiv(s, len(a))
    s = [math.sqrt(it) for it in s]
    return s

def vaggregate1(st1, st2):
    n1, m1, s1 = st1
    n2, m2, s2 = st2
    n = n1 + n2
    m = vdiv(vsum(vmul(m1, n1), vmul(m2, n2)), n)
    s = math.sqrt((2 * n1 * vscalar(m1, vsub(m1, m)) + 2 * n2 * vscalar(m2, vsub(m2, m)) +  n * vscalar(m, m) - n1 * vscalar(m1, m1) - n2 * vscalar(m2, m2) + n1 * s1 ** 2 + n2 * s2 ** 2) / n)
    return n, m, s

def vaggregate2(st1, st2):
    n1, m1, s1 = st1
    n2, m2, s2 = st2
    n = n1 + n2
    m = vdiv(vsum(vmul(m1, n1), vmul(m2, n2)), n)
